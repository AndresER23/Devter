import Head from 'next/head'
import Image from 'next/image'
import AppLayout from '../components/AppLayout'
import { colors } from '../styles/theme'
import Button from '../components/button'
import GitHub from '../components/icons/GitHub'
import { loginWithGithub, sessionState } from '../firebase/client'
import { useState, useEffect } from 'react'
import Avatar from '../components/Avatar'

export default function Home () {
  const [user, setUser] = useState(undefined)

  useEffect(() => {
    sessionState(user => {
      console.log(user)
      setUser(user)
    })
  }, [])

  function handleClick (event) {
    loginWithGithub()
      .then((user) => {
        const { avatar, username, email } = user
        setUser({ avatar, username, email })
      })
      .catch((error) => console.log(error))
  }
  return (
    <div>
      <Head>
        <title>Devter ðŸŒŽ</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <AppLayout>
        <section>
          <Image src="/devter-logo.png" alt="Logo" width={120} height={110} />
          <h1>Devter</h1>
          <h2>Talk about development with developers</h2>
          <div>
            {user === null && (
              <Button onClick={handleClick}>
                <GitHub fill={'#fff'} width={32} height={32} />
                Login with GitHub
              </Button>

            )}
            {
              user && user.avatar && (
                <div>
                  <Avatar text='este es el texto' withText={1} src={user.avatar} alt={'loged-user'}></Avatar>
                </div>
              )
            }
          </div>
        </section>
      </AppLayout>

      <style jsx>{`
        div {
          margin-top: 15px;
        }
        h1 {
          color: ${colors.primary};
        }
        h2 {
          color: ${colors.secondary};
        }
        section {
          display: grid;
          place-items: center;
          place-content: center;
          height: 100%;
        }
      `}</style>
    </div>
  )
}
